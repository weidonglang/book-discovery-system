  function getLanguage() {
    const stored = window.localStorage.getItem(STORAGE_KEY);
    return SUPPORTED_LANGUAGES.includes(stored) ? stored : DEFAULT_LANGUAGE;
  }

  function resolveKey(language, key) {
    return String(key || '')
      .split('.')
      .reduce((value, segment) => (value && value[segment] !== undefined ? value[segment] : undefined), dictionaries[language]);
  }

  function interpolate(template, variables = {}) {
    return String(template ?? '').replace(/\{\{(\w+)\}\}/g, (_, token) => {
      return variables[token] !== undefined && variables[token] !== null ? String(variables[token]) : '';
    });
  }

  function t(key, variables = {}) {
    const language = getLanguage();
    const value = resolveKey(language, key) ?? resolveKey(DEFAULT_LANGUAGE, key) ?? key;
    return typeof value === 'string' ? interpolate(value, variables) : value;
  }

  function applyBinding(binding) {
    const elements = binding.all
      ? Array.from(document.querySelectorAll(binding.selector))
      : [document.querySelector(binding.selector)].filter(Boolean);

    elements.forEach(element => {
      const value = t(binding.key, binding.vars);
      if (binding.attr) {
        element.setAttribute(binding.attr, value);
      } else if (binding.html) {
        element.innerHTML = value;
      } else {
        element.textContent = value;
      }
    });
  }

  function applyI18n() {
    document.documentElement.lang = getLanguage();
    const page = document.body?.dataset?.page;
    (pageBindings[page] || []).forEach(applyBinding);
  }

  function setLanguage(language, options = {}) {
    const nextLanguage = SUPPORTED_LANGUAGES.includes(language) ? language : DEFAULT_LANGUAGE;
    window.localStorage.setItem(STORAGE_KEY, nextLanguage);
    document.documentElement.lang = nextLanguage;
    if (options.reload === false) {
      applyI18n();
      return;
    }
    window.location.reload();
  }

  window.BookI18n = {
    getLanguage,
    getLocale: getLanguage,
    isChinese: () => getLanguage() === 'zh-CN',
    setLanguage,
    t,
    applyI18n,
    dictionaries,
    pageBindings
  };
